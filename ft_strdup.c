#include "libft.h"

char	*ft_strdup(const char *s)
{
	size_t	size;
	char	*ptr;

	size = ft_strlen(s) + 1;
	ptr = (char *)malloc(size);
	if (!ptr)
		return (NULL);
	return (ft_memcpy(ptr, s, size));
}
