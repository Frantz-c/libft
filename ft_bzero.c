#include "libft.h"

void	ft_bzero(void *b, size_t n)
{
	uint64_t	ptr;

	ptr = (uint64_t)b;
	while (n > 7)
	{
		*((uint64_t *)ptr) = 0;
		ptr += 8;
		n -= 8;
	}
	while (n--)
	{
		*((uint8_t *)ptr) = 0;
		ptr++;
	}
}
