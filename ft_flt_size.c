#include "libft.h"

int		ft_flt_size(long double n)
{
	int		size;

	n = (n < 0.0) ? -n : n;
	size = 0;
	while (1)
	{
		size++;
		if ((n /= 10.0) < 1.0)
			break ;
	}
	return (size);
}
